// 0x80 = 0b 1000 0000
inline uint8_t AES_128::xtime(uint8_t val) {
	return (val & 0x80) ? ((val << 1) ^ 0x1b) : (val << 1);
}

void AES_128::sub_bytes(uint8_t *state) {
	for (int i = 0; i < AES_BLOCK_SIZE; i++) {
		state[i] = SBOX[state[i]];
	}
}

void AES_128::inv_sub_bytes(uint8_t *state) {
	for (int i = 0; i < AES_BLOCK_SIZE; i++) {
		state[i] = INV_SBOX[state[i]];
	}
}

/*	Shift Rows
	=================
	0 4 8  12	Row 0 | 0  4  8  12	 Row 0
	1 5 9  13	Row 1 | 5  9  13 1   Row 1
	2 6 10 14	Row 2 | 10 14 2  6	 Row 2
	3 7 11 15	Row 3 | 15 3  7  11  Row 3 
	=================
*/
void AES_128::shift_rows(uint8_t *state) {
	uint8_t temp;
	// Row 1
	temp = state[1];
	state[1] = state[5];
	state[5] = state[9];
	state[9] = state[13];
	state[13] = temp;
	// Row 2
	temp = state[2];
	state[2] = state[10];
	state[10] = temp;
	temp = state[6];
	state[6] = state[14];
	state[14] = temp;
	// Row 3
	temp = state[15];
	state[15] = state[11];
	state[11] = state[7];
	state[7] = state[3];
	state[3] = temp;
}

void AES_128::inv_shift_rows(uint8_t *state) {
	uint8_t temp;
	// Row 1
	temp = state[13];
	state[13] = state[9];
	state[9] = state[5];
	state[5] = state[1];
	state[1] = temp;
	// Row 2
	temp = state[2];
	state[2] = state[10];
	state[10] = temp;
	temp = state[6];
	state[6] = state[14];
	state[14] = temp;
	// Row 3
	temp = state[3];
	state[3] = state[7];
	state[7] = state[11];
	state[11] = state[15];
	state[15] = temp;
}

/*	Mix Columns
	https://en.wikipedia.org/wiki/Rijndael_MixColumns
	==============================
	d0 = 2 b0 + 3 b1 + 1 b2 + 1 b3;
	d1 = 1 b0 + 2 b1 + 3 b2 + 1 b3;
	d2 = 1 b0 + 1 b1 + 2 b2 + 3 b3;
	d3 = 3 b0 + 1 b1 + 1 b2 + 2 b3;
*/
void AES_128::mix_columns(uint8_t *state) {
	uint8_t temp;
	uint8_t temp_state[16];

	for (int i = 0; i < AES_BLOCK_SIZE; i += 4) {
		temp = state[i] ^ state[i + 1] ^ state[i + 2] ^ state[i + 3];

		temp_state[i]	  = xtime(state[i] ^ state[i + 1])	   ^ state[i]	  ^ temp;
		temp_state[i + 1] = xtime(state[i + 1] ^ state[i + 2]) ^ state[i + 1] ^ temp;
		temp_state[i + 2] = xtime(state[i + 2] ^ state[i + 3]) ^ state[i + 2] ^ temp;
		temp_state[i + 3] = xtime(state[i + 3] ^ state[i])	   ^ state[i + 3] ^ temp;
	}

	for (int i = 0; i < AES_BLOCK_SIZE; i++) {
		state[i] = temp_state[i];
	}
}

